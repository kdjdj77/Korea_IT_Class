package loginTest;

public class User {
	// alt + shift + a 를 누르면 마우스 커서의 크기를 조정하여 한 번에 입력할 수 있다
	private String id;
	private String pw;
	private String name;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
